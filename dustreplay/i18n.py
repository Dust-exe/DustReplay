def lang_code() -> str:
    c = (config.get("ui_language") or "en").lower()
    return c if c in _MSG else "en"


def t(key: str, **kwargs) -> str:
    lc = lang_code()
    s = _MSG.get(lc, _MSG["en"]).get(key)
    if s is None:
        s = _MSG["en"].get(key, key)
    if kwargs:
        try:
            return s.format(**kwargs)
        except Exception:
            return s
    return s


